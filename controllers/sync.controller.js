// TH√äM: ƒê·ªãnh nghƒ©a m√∫i gi·ªù c·ª≠a h√†ng (PH·∫¢I TR√ôNG V·ªöI HARAVAN)
const STORE_TIMEZONE = process.env.STORE_TIMEZONE || 'Asia/Ho_Chi_Minh'; // V√≠ d·ª• cho Vi·ªát Nam (GMT+7)

// H√†m tr·ª£ gi√∫p ƒë·ªÉ ki·ªÉm tra xem m·ªôt s·∫£n ph·∫©m c√≥ kh·ªõp v·ªõi quy t·∫Øc c·ªßa Smart Collection kh√¥ng
const matchesRule = (product, rule) => {
    const { column, relation, condition } = rule;
    let productValue;

    switch (column) {
        case 'title':
            productValue = product.title;
            break;
        case 'product_type':
            productValue = product.product_type;
            break;
        case 'vendor':
            productValue = product.vendor;
            break;
        case 'tag':
            // Rule c√≥ th·ªÉ l√† 'tag' (string) v√† product.tags c≈©ng l√† string.
            // C·∫ßn chuy·ªÉn tags c·ªßa s·∫£n ph·∫©m th√†nh m·∫£ng ƒë·ªÉ ki·ªÉm tra kh·ªõp
            productValue = product.tags ? product.tags.split(',').map(tag => tag.trim()) : [];
            break;
        case 'variant_title':
            productValue = product.variants.map(v => v.title).join(', '); 
            break;
        case 'price':
            // ƒê·ªÉ ƒë∆°n gi·∫£n, l·∫•y gi√° c·ªßa variant ƒë·∫ßu ti√™n ho·∫∑c gi√° trung b√¨nh
            productValue = product.variants.length > 0 ? product.variants[0].price : 0; 
            break;
        case 'compare_at_price':
            productValue = product.variants.length > 0 ? product.variants[0].compare_at_price : 0;
            break;
        case 'variant_weight': // N·∫øu c√≥ variant.grams
            productValue = product.variants.length > 0 ? product.variants[0].grams : 0;
            break;
        case 'inventory_quantity': // N·∫øu c√≥ variant.inventory_quantity
            productValue = product.variants.reduce((sum, v) => sum + (v.inventory_quantity || 0), 0);
            break;
        default:
            console.warn(`‚ö†Ô∏è [Sync] Quy t·∫Øc kh√¥ng x√°c ƒë·ªãnh trong Smart Collection: ${column}`);
            return false;
    }

    if (productValue === undefined || productValue === null) {
        return false;
    }

    const conditionValue = String(condition).toLowerCase(); // ƒê·∫£m b·∫£o condition l√† chu·ªói
    let isMatch = false;

    // X·ª≠ l√Ω c√°c ki·ªÉu quan h·ªá
    switch (relation) {
        case 'equals':
            if (column === 'tag') {
                isMatch = productValue.includes(conditionValue);
            } else if (column === 'price' || column === 'compare_at_price' || column === 'inventory_quantity' || column === 'variant_weight') {
                isMatch = parseFloat(productValue) === parseFloat(conditionValue);
            } else {
                isMatch = String(productValue).toLowerCase() === conditionValue;
            }
            break;
        case 'not_equals':
            if (column === 'tag') {
                isMatch = !productValue.includes(conditionValue);
            } else if (column === 'price' || column === 'compare_at_price' || column === 'inventory_quantity' || column === 'variant_weight') {
                isMatch = parseFloat(productValue) !== parseFloat(conditionValue);
            } else {
                isMatch = String(productValue).toLowerCase() !== conditionValue;
            }
            break;
        case 'contains':
            if (column === 'tag') {
                isMatch = productValue.some(tag => tag.includes(conditionValue));
            } else {
                isMatch = String(productValue).toLowerCase().includes(conditionValue);
            }
            break;
        case 'not_contains':
            if (column === 'tag') {
                isMatch = !productValue.some(tag => tag.includes(conditionValue));
            } else {
                isMatch = !String(productValue).toLowerCase().includes(conditionValue);
            }
            break;
        case 'starts_with':
            isMatch = String(productValue).toLowerCase().startsWith(conditionValue);
            break;
        case 'ends_with':
            isMatch = String(productValue).toLowerCase().endsWith(conditionValue);
            break;
        case 'greater_than':
            isMatch = parseFloat(productValue) > parseFloat(conditionValue);
            break;
        case 'less_than':
            isMatch = parseFloat(productValue) < parseFloat(conditionValue);
            break;
        default:
            console.warn(`‚ö†Ô∏è [Sync] Quan h·ªá quy t·∫Øc kh√¥ng x√°c ƒë·ªãnh: ${relation}`);
            return false;
    }
    return isMatch;
};

async function syncAllData(req, res) {
    console.log('üîÑ B·∫Øt ƒë·∫ßu qu√° tr√¨nh ƒë·ªìng b·ªô d·ªØ li·ªáu...');
    try {
        const [
            couponsFromHaravan, 
            ordersFromHaravan, 
            customersFromHaravan,
            productsFromHaravan,
            smartCollectionsFromHaravan // <-- C·∫¨P NH·∫¨T: Ch·ªâ l·∫•y Smart Collections
            // X√ìA: collectsFromHaravan
        ] = await Promise.all([
            haravanService.getDiscountCodes(),
            haravanService.getOrders(), 
            haravanService.getCustomers(),
            haravanService.getProducts(),
            haravanService.getSmartCollections(), // <-- C·∫¨P NH·∫¨T
            // X√ìA: haravanService.getCollects()
            Promise.resolve([]) // <-- Thay th·∫ø getCollects b·∫±ng m·ªôt Promise r·ªóng ƒë·ªÉ gi·ªØ c·∫•u tr√∫c Promise.all
        ]);

        console.log(`- ƒê√£ l·∫•y ƒë∆∞·ª£c: ${productsFromHaravan.length} s·∫£n ph·∫©m, ${couponsFromHaravan.length} m√£, ${ordersFromHaravan.length} ƒë∆°n h√†ng, ${customersFromHaravan.length} kh√°ch h√†ng, ${smartCollectionsFromHaravan.length} Smart Collections.`);
        // X√ìA: collectsFromHaravan kh·ªèi log n·∫øu kh√¥ng d√πng

        // --- B∆∞·ªõc 1.5: ƒê·ªìng b·ªô Smart Collections v√†o Model MongoDB ---
        if (smartCollectionsFromHaravan && smartCollectionsFromHaravan.length > 0) {
            const collectionOps = smartCollectionsFromHaravan.map(collection => ({
                updateOne: {
                    filter: { id: collection.id },
                    update: { $set: { 
                        ...collection, 
                        created_at_haravan: collection.created_at ? utcToZonedTime(new Date(collection.created_at), STORE_TIMEZONE) : null, 
                        updated_at_haravan: collection.updated_at ? utcToZonedTime(new Date(collection.updated_at), STORE_TIMEZONE) : null 
                    } },
                    upsert: true
                }
            }));
            await HaravanCollection.bulkWrite(collectionOps);
            console.log(`‚úÖ ƒê√£ ƒë·ªìng b·ªô ${smartCollectionsFromHaravan.length} Smart Collections.`); // C·∫≠p nh·∫≠t t√™n bi·∫øn
        }
        
        // Kh√¥ng c·∫ßn collectionIdToNameMap hay productCollectsMap ·ªü ƒë√¢y n·ªØa
        // v√¨ Product s·∫Ω t·ª± t√≠nh to√°n membership d·ª±a tr√™n rules.

        // --- B∆∞·ªõc 2: ƒê·ªìng b·ªô Products v√† √°nh x·∫° v·ªõi Smart Collections ---
        if (productsFromHaravan && productsFromHaravan.length > 0) {
            const productOps = productsFromHaravan.map(product => {
                const associatedCollectionIds = [];
                const associatedCollectionNames = [];

                smartCollectionsFromHaravan.forEach(collection => { // L·∫∑p qua Smart Collections
                    const { rules, disjunctive } = collection;
                    
                    // N·∫øu kh√¥ng c√≥ quy t·∫Øc n√†o, ho·∫∑c l·ªói d·ªØ li·ªáu, b·ªè qua collection n√†y
                    if (!rules || rules.length === 0) return; 

                    let isMatch = false;

                    if (disjunctive) { // N·∫øu disjunctive = true (OR)
                        isMatch = rules.some(rule => matchesRule(product, rule));
                    } else { // N·∫øu disjunctive = false (AND)
                        isMatch = rules.every(rule => matchesRule(product, rule));
                    }

                    if (isMatch) {
                        associatedCollectionIds.push(collection.id);
                        associatedCollectionNames.push(collection.title);
                    }
                });

                return {
                    updateOne: {
                        filter: { id: product.id },
                        update: {
                            $set: {
                                ...product,
                                // Chu·∫©n h√≥a created_at_haravan v√† updated_at_haravan
                                created_at_haravan: product.created_at ? utcToZonedTime(new Date(product.created_at), STORE_TIMEZONE) : null,
                                updated_at_haravan: product.updated_at ? utcToZonedTime(new Date(product.updated_at), STORE_TIMEZONE) : null,
                                haravan_collection_ids: associatedCollectionIds,
                                haravan_collection_names: associatedCollectionNames,
                                variants: product.variants.map(haravanVariant => {
                                    let newVariant = { ...haravanVariant };
                                    if (haravanVariant.cost === undefined) { 
                                        newVariant.cost = 0; 
                                    }
                                    return newVariant;
                                }),
                            },
                        },
                        upsert: true
                    }
                };
            });
            await Product.bulkWrite(productOps);
            console.log(`‚úÖ ƒê√£ ƒë·ªìng b·ªô ${productsFromHaravan.length} s·∫£n ph·∫©m.`);
        }

        // --- B∆∞·ªõc 3: ƒê·ªìng b·ªô M√£ gi·∫£m gi√° ---
        if (couponsFromHaravan && couponsFromHaravan.length > 0) {
            const couponOps = couponsFromHaravan.map(coupon => ({
                updateOne: {
                    filter: { id: coupon.id },
                    update: { $set: coupon },
                    upsert: true
                }
            }));
            await Coupon.bulkWrite(couponOps); 
            console.log(`‚úÖ ƒê√£ ƒë·ªìng b·ªô ${couponsFromHaravan.length} m√£ gi·∫£m gi√°.`);
        }

        // --- B∆∞·ªõc 4: ƒê·ªìng b·ªô ƒê∆°n h√†ng (ƒê√É S·ª¨A L·ªñI X·ª¨ L√ù created_at_haravan) ---
        if (ordersFromHaravan && ordersFromHaravan.length > 0) {
            const orderOps = ordersFromHaravan.map(order => {
                // Parse created_at t·ª´ chu·ªói Haravan (th∆∞·ªùng l√† ISO 8601 UTC)
                const haravanCreatedAtUTC = order.created_at ? new Date(order.created_at) : null;
                
                // Chu·∫©n h√≥a created_at_haravan (UTC) sang m√∫i gi·ªù c·ª•c b·ªô c·ªßa c·ª≠a h√†ng (GMT+7)
                const createdDateTimeInStoreTimezone = haravanCreatedAtUTC ? utcToZonedTime(haravanCreatedAtUTC, STORE_TIMEZONE) : null;

                // Log ƒë·ªÉ ki·ªÉm tra gi√° tr·ªã n√†y (debug)
                console.log(`ƒê∆°n h√†ng ${order.id}: created_at_haravan t·ª´ Haravan (RAW): ${order.created_at} -> Date Object (UTC): ${haravanCreatedAtUTC?.toISOString()} -> Store Timezone Date: ${createdDateTimeInStoreTimezone?.toISOString()} (Locale: ${createdDateTimeInStoreTimezone?.toLocaleString('vi-VN', {timeZone: STORE_TIMEZONE})})`);


                return {
                    updateOne: {
                        filter: { id: order.id },
                        update: { 
                            $set: { 
                                ...order, 
                                created_at_haravan: createdDateTimeInStoreTimezone, 
                                updated_at_haravan: order.updated_at ? utcToZonedTime(new Date(order.updated_at), STORE_TIMEZONE) : null,
                                cancelled_at: order.cancelled_at ? utcToZonedTime(new Date(order.cancelled_at), STORE_TIMEZONE) : null,
                            } 
                        },
                        upsert: true
                    }
                };
            });
            await Order.bulkWrite(orderOps); 
            console.log(`‚úÖ ƒê√£ ƒë·ªìng b·ªô ${ordersFromHaravan.length} ƒë∆°n h√†ng.`);
        }

        // --- B∆∞·ªõc 5: ƒê·ªìng b·ªô Kh√°ch h√†ng ---
        if (customersFromHaravan && customersFromHaravan.length > 0) {
            const customerOps = customersFromHaravan.map(customer => ({
                updateOne: {
                    filter: { id: customer.id },
                    update: { $set: customer },
                    upsert: true
                }
            }));
            await Customer.bulkWrite(customerOps); 
            console.log(`‚úÖ ƒê√£ ƒë·ªìng b·ªô ${customersFromHaravan.length} kh√°ch h√†ng.`);
        }

        res.status(200).json({
            message: 'üéâ ƒê·ªìng b·ªô d·ªØ li·ªáu th√†nh c√¥ng!',
            syncedProducts: productsFromHaravan.length,
            syncedCoupons: couponsFromHaravan.length,
            syncedOrders: ordersFromHaravan.length,
            syncedCustomers: customersFromHaravan.length,
        });

    } catch (error) {
        console.error('‚ùå L·ªói trong qu√° tr√¨nh ƒë·ªìng b·ªô:', error);
        if (error.response) {
            console.error('Ph·∫£n h·ªìi l·ªói t·ª´ Haravan API:', error.response.data);
        }
        res.status(500).json({ message: 'ƒê·ªìng b·ªô d·ªØ li·ªáu th·∫•t b·∫°i.', error: error.message });
    }
}

